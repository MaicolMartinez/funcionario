    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        String tipo_de_documento = jTextTipoDocum.getText();
        int numero_de_documento = Integer.parseInt(jTextNumDocum.getText());
        String nombres = jTextNombres.getText();
        String apellidos = jTextApellidos.getText();
        String estado_civil = jTextEstCivil.getText();
        String genero = jTextGenero.getText();
        String direccion = jTextDirec.getText();
        String fecha_de_nacimiento = jTextFecha.getText();
        String informacion_academica = jTextInfoAcade.getText();

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        java.sql.Date fechaConvertida = null;

        try {
            java.util.Date parsed = dateFormat.parse(fecha_de_nacimiento);
            fechaConvertida = new java.sql.Date(parsed.getTime());
        } catch (Exception e) {
            System.out.println("Error occurred" + e.getMessage());
        }

        try {
            conexion objetoConexion = new conexion();

            PreparedStatement ps = objetoConexion.getConexion().prepareStatement("INSERT INTO funcionarios (tipo_de_documento, numero_de_documento, nombres, apellidos, estado_civil, genero, direccion, fecha_de_nacimiento, informacion_academica) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)");

            ps.setString(1, tipo_de_documento);
            ps.setInt(2, numero_de_documento);
            ps.setString(3, nombres);
            ps.setString(4, apellidos);
            ps.setString(5, estado_civil);
            ps.setString(6, genero);
            ps.setString(7, direccion);
            ps.setDate(8, fechaConvertida);
            ps.setString(9, informacion_academica);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "datos guardados en funcionario");
            limpiar();
            cargarDatos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

        try {
            conexion objetoConexion = new conexion();

            PreparedStatement ps = objetoConexion.getConexion().prepareStatement("INSERT INTO genero (numero_de_documento, genero) VALUES (?, ?)");

            ps.setInt(1, numero_de_documento);
            ps.setString(2, genero);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "datos guardados en genero");
            limpiar();
            cargarDatos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }


    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jTableFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionariosMouseClicked
        // TODO add your handling code here:

        try {
            int fila = jTableFuncionarios.getSelectedRow();
            int id = Integer.parseInt(jTableFuncionarios.getValueAt(fila, 0).toString());

            PreparedStatement ps;
            ResultSet rs;

            conexion objetoConexion = new conexion();
            ps = objetoConexion.getConexion().prepareStatement("SELECT tipo_de_documento, numero_de_documento, nombres, apellidos, estado_civil, genero, direccion, fecha_de_nacimiento, informacion_academica FROM funcionarios WHERE id_funcionario=? ");

            ps.setInt(1, id);
            rs = ps.executeQuery();

            while (rs.next()) {

                jTextId.setText(String.valueOf(id));
                jTextTipoDocum.setText(rs.getString("tipo_de_documento"));
                jTextNumDocum.setText(rs.getString("numero_de_documento"));
                if (!jTextNumDocum.getText().isEmpty()) {
                    jTextNumDocum.setEnabled(false);
                } else {
                    jTextNumDocum.setEnabled(true);
                }
                jTextNombres.setText(rs.getString("nombres"));
                jTextApellidos.setText(rs.getString("apellidos"));
                jTextEstCivil.setText(rs.getString("estado_civil"));
                jTextGenero.setText(rs.getString("genero"));
                jTextDirec.setText(rs.getString("direccion"));
                jTextFecha.setText(rs.getString("fecha_de_nacimiento"));
                jTextInfoAcade.setText(rs.getString("informacion_academica"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jTableFuncionariosMouseClicked

