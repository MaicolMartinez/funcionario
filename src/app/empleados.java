    private void cargarDatos() {

        DefaultTableModel modeloTabla = (DefaultTableModel) jTableFuncionarios.getModel();
        modeloTabla.setRowCount(0);

        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;

        try {

            conexion objetoConexion = new conexion();

            ps = objetoConexion.getConexion().prepareStatement("SELECT id_funcionario, tipo_de_documento, numero_de_documento, nombres, apellidos FROM funcionarios");

            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();

            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int indice = 0; indice < columnas; indice++) {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }

